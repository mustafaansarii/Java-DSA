package helloaworld;

public class string_tuto {
	public static void main(String[] args) {
		String name1="Amana";
		String name2="Sonu";
		System.out.println(name1+" and " +name2);
		
		//char
		String name="Aman";
		System.out.println(name.charAt(3));
		//replace
		System.out.println(name.replace("A", "s"));
		String name4="Aman and samna";
		System.out.println(name4.substring(0,4));
		System.out.println(name4.substring(4,14));
		
	}

}
